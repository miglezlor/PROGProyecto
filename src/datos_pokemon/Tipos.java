package datos_pokemon;

/**
 *
 * @author mgonzalezlorenzo
 */
public class Tipos {
    private String nomTip;
    private int mult;
    
    
    
    public Tipos() {
    }

    public Tipos(String nomTip) {
        this.nomTip = nomTip;
    }

    public String getNomTip() {
        return nomTip;
    }

    public void setNomTip(String nomTip) {
        this.nomTip = nomTip;
    }

    public int getMult() {
        return mult;
    }

    public void setMult(int mult) {
        this.mult = mult;
    }

   
    
}
